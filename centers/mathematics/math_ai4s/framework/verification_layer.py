class LeanVerifier:
    """
    Lean形式化验证接口
    
    这是最高级别的验证：
    - 生成Lean代码
    - 调用lean --make验证
    - 确保零error、零warning
    """
    
    def __init__(self, lean_path: str = "lean"):
        self.lean_path = lean_path
        self.etp_path = Path.home() / "xuzhi_genesis/centers/mathematics/math_ai4s/etp_work/equational_theories"
    
    def verify(self, result: ImplicationResult) -> VerificationReport:
        """
        验证结果并生成报告
        
        遵循五条铁律进行检查
        """
        checks = FiveRulesChecker.full_check(result)
        
        passed = [k for k, v in checks.items() if v]
        failed = [k for k, v in checks.items() if not v]
        
        recommendations = []
        
        if not checks.get("rule1_computational"):
            recommendations.append("Re-run computational verification with independent tool")
        
        if not checks.get("rule2_human_review"):
            recommendations.append("Add human review notes and author attribution")
        
        if not checks.get("rule3_lean_verification") and result.status == ProofStatus.PROVEN:
            recommendations.append("Formalize proof in Lean before claiming PROVEN status")
        
        if not checks.get("rule4_understanding"):
            recommendations.append("Add understanding documentation: why is this true?")
        
        ready = len(failed) == 0 or (
            result.status == ProofStatus.CONJECTURE and 
            checks.get("rule1_computational") and
            checks.get("rule2_human_review")
        )
        
        return VerificationReport(
            result=result,
            checks_passed=passed,
            checks_failed=failed,
            recommendations=recommendations,
            ready_for_submission=ready
        )
    
    def generate_lean_file(self, results: List[ImplicationResult],
                          filename: str) -> str:
        """生成Lean文件内容"""
        
        header = f"""import equational_theories.Magma
import equational_theories.Equations.All
import equational_theories.FactsSyntax

/-! 
Auto-generated Lean file by MathAI4S Framework
Generated: {__import__('datetime').datetime.now().isoformat()}
Author: Δ (Delta-Forge)
Verification Status: See individual theorems
-/

namespace DeltaGenerated
"""
        
        theorems = []
        for result in results:
            theorem_code = result.to_lean_theorem()
            theorems.append(theorem_code)
        
        footer = """
end DeltaGenerated
"""
        
        return header + "\n".join(theorems) + footer


class VerificationPipeline:
    """
    验证流水线
    
    整合所有验证工具，提供统一的验证接口
    """
    
    def __init__(self):
        self.vampire = VampireProver()
        self.mace4 = Mace4Finder()
        self.lean = LeanVerifier()
    
    def verify_result(self, result: ImplicationResult) -> VerificationReport:
        """完整验证流程"""
        return self.lean.verify(result)
    
    def check_tools(self) -> dict:
        """检查所有工具是否可用"""
        return {
            "vampire": self.vampire.available,
            "mace4": self.mace4.available,
            "lean": True  # 假设lean可用
        }


